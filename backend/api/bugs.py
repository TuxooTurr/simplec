"""
Эндпоинт форматирования баг-репортов через LLM.
"""
import asyncio
from typing import List
from fastapi import APIRouter, HTTPException, UploadFile, File, Form
from backend.schemas import BugFormatResponse

router = APIRouter()


def _format_bug_sync(platform: str, feature: str, description: str, provider: str) -> str:
    from agents.llm_client import LLMClient, Message

    llm = LLMClient(provider=provider)

    feature_hint = f"\nФИЧА / МОДУЛЬ (подсказка): {feature.strip()}" if feature.strip() else ""

    prompt = f"""Ты — опытный QA-инженер. Ты превращаешь краткие, неформальные описания проблем (часто с ошибками, без структуры, с фрагментами JSON, логов, скринов) в чёткие структурированные дефекты (баг-репорты) по строгому шаблону для Jira.

КОМПОНЕНТ/СЛОЙ (выбран пользователем, не меняй): {platform}{feature_hint}

ВХОДНЫЕ ДАННЫЕ (могут быть неполными, с ошибками; вложения приложены ниже текстом):
{description}

═══════════════════════════════════════════════════
ПРИНЦИПЫ РАБОТЫ
═══════════════════════════════════════════════════
1. Определи, сколько отдельных дефектов содержится во входных данных. Оформи каждый отдельно по шаблону.
2. Восстанавливай логику проблемы, даже если описание неполное или с ошибками.
3. НЕ ВЫДУМЫВАЙ конкретные данные (ID, точные тексты ошибок, версии, разрешения). Всё, чего нет во входных данных, выноси в блок «Что уточнить / приложить».
4. Если во входных данных есть JSON/логи/стектрейсы — процитируй ключевые фрагменты в блоке «Артефакты» (в code-блоке), не переписывая произвольно.
5. Различай тип задачи:
   - ДЕФЕКТ — сломано то, что должно работать → акцент «фактический ≠ ожидаемый».
   - ДОРАБОТКА / НОВОЕ ТРЕБОВАНИЕ — работает, но нужно улучшить/добавить → в названии добавь префикс [Доработка], в ожидаемом результате пиши «требуется/ожидается».
6. Название строго: `[{platform}][НазваниеМодуля] Краткая суть` (до 100 символов).
   - [{platform}] — всегда из выбора пользователя. Если по содержимому слой явно другой — название не меняй, а отметь это в «Что уточнить / приложить».
   - [НазваниеМодуля] — экран, операция или модуль из контекста (например, [Авторизация], [Оплата]). Если пользователь указал фичу — используй её.
7. Шаги воспроизведения — реалистичные, как их выполнит живой тестировщик. Если есть корректный и дефектный сценарии — покажи оба («Сценарий A / Сценарий B»).
8. Критичность и приоритет связаны: приоритет обычно ≤ критичности по влиянию, но может быть выше при частой встречаемости или требовании заказчика — тогда обоснуй.
9. Раздел «Решение / Хотфикс» добавляй ТОЛЬКО если во входных данных упомянуто конкретное решение.
10. Если данных критически мало — всё равно оформи дефект: заполни известное, остальное вынеси в «Что уточнить / приложить». Не пиши выдуманных значений.
11. После всех дефектов добавь единый раздел **Дополнительная информация** с общим контекстом (контур, дата, сервис, версия и т.д.), если такие данные есть.
12. Отвечай на языке входных данных. Только Markdown. Никаких вводных слов и пояснений от себя.

═══════════════════════════════════════════════════
ШКАЛА КРИТИЧНОСТИ
═══════════════════════════════════════════════════
- Critical — система/ключевой функционал недоступны, обхода нет, массовое влияние.
- High — важный функционал заблокирован для класса данных/пользователей, регрессия, обход отсутствует или трудоёмок.
- Medium — функционал работает, но существенное неудобство/частичный дефект, есть обходной путь.
- Low — косметика, мелкие UI-замечания, не влияющие на работу.

═══════════════════════════════════════════════════
ШАБЛОН КАЖДОГО ДЕФЕКТА
═══════════════════════════════════════════════════

---

### Дефект N

**Название:** `[{platform}][НазваниеМодуля] Краткая суть`

**Описание:**
Развёрнутое описание сути проблемы: что и где происходит, при каких условиях. Если выявлена закономерность — опиши её.

**Вероятные причины:** *(только если есть обоснованная гипотеза из входных данных)*
- ...

**Окружение:**
- Среда: IFT *(если пользователь не указал иное)*
- URL: https://sber911-ift-iam.delta.sbrf.ru *(если не указано иное)*
- Прочее: роль/учётка, платформа, браузер — только если известно.

**Предусловия:**
1. ...

**Шаги воспроизведения:**
1. ...
2. ...
3. Наблюдать результат

**Фактический результат:**
Что происходит сейчас (точные фрагменты ошибок/логов — если есть). При сравнении сценариев — оформи таблицей.

**Ожидаемый результат:**
Что должно происходить (для доработки — «требуется/ожидается»).

**Артефакты:** *(только если во входных данных есть JSON/логи/скрины)*
```
метод, request/response, лог — как есть
```

**Что уточнить / приложить:** *(только если данных не хватает)*
- Точные тексты ошибок, ID сущностей, полные логи, ответы API, версия/сборка, скриншоты, ссылка на требование.

**Критичность / Приоритет:**
- Критичность: Low / Medium / High / Critical
- Приоритет: Low / Medium / High
- Обоснование: влияние на пользователя, наличие обходного пути, масштаб, регрессия.

**Решение / Хотфикс:** *(только если есть во входных данных)*
...

---"""

    response = llm.chat(
        [Message(role="user", content=prompt)],
        temperature=0.2,
        max_tokens=4000,
    )
    return response.content.strip()


@router.post("/api/bugs/format", response_model=BugFormatResponse)
async def format_bug(
    platform: str = Form(...),
    feature: str = Form(""),
    description: str = Form(...),
    provider: str = Form(...),
    attachments: List[UploadFile] = File(default=[]),
):
    from agents.file_parser import parse_file

    # Парсим вложения и добавляем их текст к описанию
    attachment_texts = []
    for f in attachments:
        if f and f.filename:
            try:
                data = await f.read()
                text = parse_file(data, f.filename)
                attachment_texts.append(f"[{f.filename}]:\n{text}")
            except Exception as e:
                attachment_texts.append(f"[{f.filename}]: не удалось прочитать — {e}")

    full_description = description
    if attachment_texts:
        full_description += "\n\n─── ВЛОЖЕНИЯ ───\n" + "\n\n".join(attachment_texts)

    provider = provider.strip()
    if not provider:
        raise HTTPException(
            status_code=400,
            detail={"message": "LLM-провайдер не выбран", "llm_error": False},
        )

    try:
        report = await asyncio.to_thread(
            _format_bug_sync,
            platform, feature, full_description, provider
        )
        return {"report": report}
    except Exception as e:
        from agents.llm_client import LLMClient
        is_llm, friendly = LLMClient.classify_error(e)
        raise HTTPException(
            status_code=503 if is_llm else 500,
            detail={"message": friendly, "llm_error": is_llm}
        )
